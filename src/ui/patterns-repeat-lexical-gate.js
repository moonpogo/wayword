  function lexemeOk(word) {
    var w = normalizeLexeme(word);
    if (!w || w.length <= 2) return false;
    if (/^\d+$/.test(w)) return false;
    if (BLOCK.has(w)) return false;
    return true;
  }

  function lexemeChallengeworthy(word, count) {
    if (!lexemeOk(word)) return false;
    var n = Number(count) || 0;
    return n >= 5;
  }

